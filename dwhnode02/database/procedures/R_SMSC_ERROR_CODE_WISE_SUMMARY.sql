 TOTAL_FAIL
from L3_SMSC P
--where SMC1_TIME_SERIAL_NUMBER
WHERE SMC1_TIME_SERIAL_NUMBER_KEY=(SELECT DATE_KEY FROM DATE_DIM WHERE DATE_VALUE = TRUNC(TO_DATE(SYSDATE-1,'DD/MM/RRRR')))
