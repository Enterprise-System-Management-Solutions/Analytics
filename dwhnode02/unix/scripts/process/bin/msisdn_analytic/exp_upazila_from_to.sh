export ORACLE_UNQNAME=DWH05
export ORACLE_BASE=/data01/app/oracle
export ORACLE_HOME=/data01/app/oracle/product/12.2.0/db_1
export ORACLE_SID=DWH05

PATH=/usr/sbin:$PATH:$ORACLE_HOME/bin

export LD_LIBRARY_PATH=$ORACLE_HOME/lib:/lib:/usr/lib;
export CLASSPATH=$ORACLE_HOME/jlib:$ORACLE_HOME/rdbms/jlib;

dt=`date -d yesterday  '+%d%m%Y'`
xdir=/tmp/moved_count_$dt.csv


sqlplus  -s <<EOF
dwh_user/dwh_user_123
SET ECHO OFF
SET WRAP OFF
SET HEAD ON
SET FEEDBACK OFF
SET TRIMSPOOL ON
SET HEADSEP ON
SET TERMOUT OFF
SET UNDERLINE OFF
SET LINESIZE 32000;
SET PAGESIZE 40000;
SET LONG 50000;
SPOOL $xdir
REM "WHENEVER SQLERROR EXIT SQL.SQLCODE"
SELECT UPZILA_FROM||'|'||UPZILA_TO||'|'||COUNTS as "UPAZILA_FROM|UPAZILA_TO|COUNTS"
FROM
(SELECT A.UPZILA_FROM,B.UPAZILA UPAZILA_FROM,A.UPZILA_TO,C.UPAZILA UPAZILA_TO, A.COUNTS
FROM MSISDN_COMPARE_FINAL A, ZONE_DIM_WITH_CODE B,ZONE_DIM_WITH_CODE C
WHERE DATE_KEY=(SELECT A.DATE_KEY FROM DATE_DIM A WHERE A.DATE_VALUE = TO_DATE (SYSDATE-1,'DD/MM/RRRR'))
AND A.UPZILA_FROM=B.UPAZILA_CODE2
AND A.UPZILA_TO=C.UPAZILA_CODE2
GROUP BY A.UPZILA_FROM,B.UPAZILA,A.UPZILA_TO,C.UPAZILA, A.COUNTS
ORDER BY 1)
/
EXIT
EOF

echo "File Exported for $dt"


